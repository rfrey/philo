#include "ft_math.h"

void		ft_pt_to_px(t_point2d *pt)
{
	int			ix;
	int			iy;

	ix = (int)pt->x;
	iy = (int)pt->y;
	pt->x = (double)ix;
	pt->y = (double)iy;
}
